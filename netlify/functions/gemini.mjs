async function getBlobsTextContext(store) {
  try {
    const raw = await store.get("_index.json");
    if (!raw) return "";
    const index = JSON.parse(raw);
    const textDocs = index.filter((f) => /\.(txt|md)$/i.test(f.nom)).slice(0, 3);
    let ctx = "";
    for (const doc of textDocs) {
      const content = await store.get(doc.nom);
      if (content) ctx += `

=== ${doc.nom} ===
${content.substring(0, 3500)}`;
    }
    return ctx;
  } catch {
    return "";
  }
}
async function getPDFInlineParts(store, docKeys) {
  try {
    let pdfs;
    if (docKeys && docKeys.length > 0) {
      // Només els documents seleccionats
      pdfs = docKeys.filter(k => /\.pdf$/i.test(k)).map(k => ({ nom: k }));
    } else {
      // Tots els PDFs de l'índex
      const raw = await store.get("_index.json", { consistency: "strong" });
      if (!raw) return [];
      const index = JSON.parse(raw);
      pdfs = index.filter((f) => /\.pdf$/i.test(f.nom)).sort((a, b) => {
        const sizeOf = (s) => parseFloat(s) * (s.includes("MB") ? 1024 * 1024 : 1024);
        return sizeOf(a.mida) - sizeOf(b.mida);
      });
    }
    const parts = [];
    let totalBytes = 0;
    const MAX_TOTAL = 10 * 1024 * 1024;
    for (const doc of pdfs) {
      try {
        const buffer = await store.get(doc.nom, { type: "arrayBuffer", consistency: "strong" });
        if (!buffer) continue;
        if (totalBytes + buffer.byteLength > MAX_TOTAL) continue;
        totalBytes += buffer.byteLength;
        const data = Buffer.from(buffer).toString("base64");
        parts.push({ text: `\n[Document: ${doc.nom}]\n` });
        parts.push({ inline_data: { mime_type: "application/pdf", data } });
      } catch {
      }
    }
    return parts;
  } catch {
    return [];
  }
}
async function handler(req) {
  if (req.method === "OPTIONS") return new Response(null, { status: 204, headers: CORS });
  if (req.method !== "POST") return new Response("Method not allowed", { status: 405, headers: CORS });
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    return new Response(
      JSON.stringify({
        error: "GEMINI_API_KEY no configurada",
        setup: "Afegeix-la a Netlify \u2192 Site configuration \u2192 Environment variables \u2192 GEMINI_API_KEY i redesplega."
      }),
      { status: 503, headers: { ...CORS, "Content-Type": "application/json" } }
    );
  }
  try {
    const body = await req.json();
    const { messages, searchMode, docKeys, extractMode, extractStatus, translateStatus } = body;
    const store = getStore("normativa-docs");

    // ── Extract mode: comprova cache de Blobs (l'extracció real fa extract-background) ──
    if (extractMode) {
      const docKey = docKeys?.[0];
      if (!docKey) return Response.json({ error: "Falta docKey" }, { headers: CORS });
      const cacheKey = `_art_${docKey}`;
      try {
        const cached = await store.get(cacheKey, { type: "text" });
        if (cached) return Response.json({ articles: JSON.parse(cached), fromCache: true }, { headers: CORS });
      } catch { }
      return Response.json({ articles: [], status: "not_cached" }, { headers: CORS });
    }

    // ── Extract status: comprova progrés de l'extracció en background ──
    if (extractStatus) {
      const docKey = docKeys?.[0];
      if (!docKey) return Response.json({ status: "not_started" }, { headers: CORS });
      // Comprova PRIMER si hi ha un procés actiu (té prioritat sobre la caché)
      try {
        const statusRaw = await store.get(`_art_status_${docKey}`, { type: "text" });
        if (statusRaw) return Response.json(JSON.parse(statusRaw), { headers: CORS });
      } catch { }
      // Articles ja extrets (i no hi ha procés actiu)?
      try {
        const cached = await store.get(`_art_${docKey}`, { type: "text" });
        if (cached) return Response.json({ articles: JSON.parse(cached), status: "done" }, { headers: CORS });
      } catch { }
      return Response.json({ status: "not_started" }, { headers: CORS });
    }

    // ── Translate status: comprova progrés de la traducció en background ──
    if (translateStatus) {
      const docKey = docKeys?.[0];
      if (!docKey) return Response.json({ status: "not_started" }, { headers: CORS });
      // Comprova PRIMER si hi ha un procés actiu
      try {
        const statusRaw = await store.get(`_art_ca_status_${docKey}`, { type: "text" });
        if (statusRaw) return Response.json(JSON.parse(statusRaw), { headers: CORS });
      } catch { }
      // Traducció ja feta (i no hi ha procés actiu)?
      try {
        const cached = await store.get(`_art_ca_${docKey}`, { type: "text" });
        if (cached) return Response.json({ articles: JSON.parse(cached), status: "done" }, { headers: CORS });
      } catch { }
      return Response.json({ status: "not_started" }, { headers: CORS });
    }

    if (!Array.isArray(messages) || messages.length === 0) {
      return new Response("Missatges buits", { status: 400, headers: CORS });
    }
    let contents;
    if (searchMode) {
      const pdfParts = await getPDFInlineParts(store, docKeys);
      const userQuery = messages[messages.length - 1]?.content || "";
      const userParts = [
        { text: SEARCH_SYSTEM_PROMPT + "\n\nPregunta de cerca: " + userQuery },
        ...pdfParts
      ];
      contents = [{ role: "user", parts: userParts }];
    } else {
      const blobsCtx = await getBlobsTextContext(store);
      contents = messages.map((m, i) => {
        let text = m.content;
        if (i === 0 && m.role === "user") {
          text = SYSTEM_PROMPT + (blobsCtx ? `

=== DOCUMENTS AL REPOSITORI DE NORMATIVA ===
${blobsCtx}

` : "\n\n") + text;
        }
        return {
          role: m.role === "user" ? "user" : "model",
          parts: [{ text }]
        };
      });
    }
    const resp = await fetch(GEMINI_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json", "x-goog-api-key": apiKey },
      body: JSON.stringify({
        contents,
        generationConfig: {
          temperature: searchMode ? 0.2 : 0.4,
          maxOutputTokens: searchMode ? 1500 : 2500,
          topP: 0.9
        },
        safetySettings: [
          { category: "HARM_CATEGORY_DANGEROUS_CONTENT", threshold: "BLOCK_NONE" }
        ]
      })
    });
    if (!resp.ok) {
      const errBody = await resp.text();
      throw new Error(`Gemini ${resp.status}: ${errBody.substring(0, 300)}`);
    }
    const data = await resp.json();
    const reply = data.candidates?.[0]?.content?.parts?.[0]?.text ?? "No he pogut generar una resposta. Prova-ho de nou.";
    return Response.json({ reply }, { headers: CORS });
  } catch (err) {
    console.error("[gemini]", err);
    return new Response(
      JSON.stringify({ error: err.message }),
      { status: 500, headers: { ...CORS, "Content-Type": "application/json" } }
    );
  }
}
